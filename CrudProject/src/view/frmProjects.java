    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        proj.setNome(txtName.getText());
        //proj.setDescricao(txtDescription.getText());
        proj.setResponsavel(txtResponsible.getText());
        proj.Cadastrar();
        Consultar();
        
        ResultSet tabela;
          tabela = null;
          
          tabela = proj.Consultar();
          DefaultTableModel modelo = (DefaultTableModel) tblProjects.getModel();
          modelo.setNumRows(0);
          
          try{
              do{
                  modelo.addRow(new String[] {tabela.getString(1),tabela.getString(2),tabela.getString(3),tabela.getString(4)});
              }
              while(tabela.next());
          }
          catch(SQLException erro)
          {
              JOptionPane.showMessageDialog(null, "Erro ao preeencher a tabela" + erro);
          }
      
    }//GEN-LAST:event_btnRegisterActionPerformed

