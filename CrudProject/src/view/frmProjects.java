    public void consultarEspecifico(){
    
          ResultSet tabela;
          tabela = null;
          
          tabela =proj.consultarCompoExpecifico();
          DefaultTableModel modelo = (DefaultTableModel) tblProjects.getModel();
          modelo.setNumRows(0);
          
          try{
              do{
                  modelo.addRow(new String[] {tabela.getString(1),tabela.getString(2),tabela.getString(3),tabela.getString(4)});
              }
              while(tabela.next());
          }
          catch(SQLException erro)
          {
              JOptionPane.showMessageDialog(null, "Erro ao preeencher a tabela" + erro);
          }
    }  
